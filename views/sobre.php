                <div align="justify">O Bazar Cherburgo é uma empresa familiar que inicialmente tinha como foco a revenda de produtos. Seu proprietario teve uma atitude empreendedora ao reunir seu capital e se lancar neste negocio proprio, que logo cresceria e se tornaria bem evoluido comercialmente. O Bazar Cherburgo, foi criado no ano de 1999, localizado em um bairro da Zona Oeste, chamado Padre Miguel, no Rio de Janeiro. Seu objetivo é fazer com que clientes de todo o Rio de Janeiro, possam comprar utilidades para o lar, cama, mesa e banho, brinquedos, entre outras mercadorias, sendo atendidos sempre da melhor maneira possível, se tornando assim, clientes fiéis do Bazar Cherburgo. A hierarquia funcional da empresa é composta por seu proprietario, responsável por tomar as decisões das compras, pagamentos, administrações, seguidos de dois funcionários, um na parte da manhã e o outro na parte da tarde, o estabelecimento atende em média 200 clientes por semana. A estrutura da empresa ainda não é informatizada, mas a posição estratégica no bairro lhe permite acesso e facilidade para a implantação de um sistema tecnológico. Sendo assim com o passar do tempo e para obter maior sucesso, o proprietário do estabelecimento adotou ao sistema SGB (Sistema Gerencial para Bazares), com o objetivo de agilizar o atendimento ao cliente, ter um melhor controle das vendas, sendo assim melhorando o controle do seu estoque. Fazendo com que todo aquele processo antes feito manualmente pudesse ser controlado por um sistema específico e adequado para as necessidades.
